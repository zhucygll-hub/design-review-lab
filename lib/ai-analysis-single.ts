function buildWorkFormEvaluationGuide(workForm: WorkForm): string {
  switch (workForm) {
    case 'board':
      return `【作品形式：展板 — 评价标准】
展板是设计成果的综合展示媒介。评价重点：
- 版式组织：阅读顺序、图文比例、网格对齐、视觉重心是否明确
- 信息传达：标题层级、文字可读性、图纸/渲染与文字说明的对应关系
- 叙事逻辑：从问题到方案的推导过程是否清晰可见
- 专业呈现：图纸密度、渲染质量、色彩统一性、细节标注完整性

红牌规则全部适用（无排版网格/配色无逻辑/字体混乱/图片质量差/可读性差/模板化无创新/视觉审美不足）。
若展板中缺少某类信息（如过程草图），可作为观察指出，但不是必须有的内容。`

    case 'physical_model':
      return `【作品形式：模型实物照片 — 评价标准】
这是一张模型实物的照片。你只能评价照片中可见的内容。禁止把"照片里看不到"当成"学生没做"。

✅ 可以评价的（必须在照片中可见）：
- 造型语言：形态是否明确？体块关系是否协调？比例是否合理？曲面/线条质量如何？
- 结构与工艺：结构是否合理？表面处理、接缝、材质表达、制作精度如何？
- 展示质量：拍摄角度是否展示了模型关键特征？光线是否呈现造型体量？背景是否干扰？
- 设计表达：模型形态是否传达了设计意图和概念方向？是否体现了审美判断？

❌ 不能评价的（照片中无法看到，禁止作为缺点扣分）：
- 用户调研过程、Persona、用户定位 → 模型照片无法展示
- 设计推导过程、草图迭代、头脑风暴 → 除非照片中有展板或说明文档
- 市场分析、商业可行性、竞品研究 → 除非有配套说明
- 交互流程、信息架构 → 除非是APP/UI原型模型
- "缺少概念推导"、"缺少用户定位"、"缺少调研支撑" → 绝对禁止写这类评价

⚠️ 不确定时：说"从当前照片无法判断XX"，而不是假设它缺失或不足。

红牌规则调整：
- "无排版网格" → 不适用于模型实物，除非模型表面有可见的排版/文字
- "字体混乱" → 不适用于模型实物，除非模型上有可见字体
- "可读性差" → 不适用于模型实物，除非模型上有需要阅读的文字
- "模板化无创新" → 仅当模型形态明显为现成产品/方案拼凑时才适用
- "配色无逻辑" → 仅当模型表面涂装/色彩处理明显无理时适用
- "视觉审美不足"、"图片质量差" → 正常适用`

    case 'poster':
      return `【作品形式：海报 — 评价标准】
评价重点：
- 视觉冲击力：主视觉是否能在远距离/快速浏览中抓住注意力？
- 信息层级：标题、副标题、正文、日期、地点等层级是否分明？
- 主题表达：创意概念是否与传播目标匹配？情绪传达是否准确？
- 色彩与字体：配色策略、字体选择是否服务于主题和受众？
- 传播适配：在不同尺寸/距离/媒介下是否保持可读性和记忆点？

红牌规则全部适用。海报对"可读性差"和"字体混乱"特别敏感。`

    case 'ui':
      return `【作品形式：UI界面 — 评价标准】
评价重点：
- 信息架构：页面结构是否合理？导航是否清晰可预测？
- 交互逻辑：操作流程是否顺畅？状态反馈（加载/空/错误/成功）是否完整？
- 视觉一致性：组件风格、间距、色彩、圆角、阴影是否统一？
- 可用性：关键操作是否易发现？文字是否可读？触控区域是否合理？
- 界面层级：主次信息是否通过大小、颜色、位置有效区分？

红牌规则全部适用。"可读性差"和"无排版网格"对UI是严重缺陷。
若只展示单张界面截图而无法查看完整流程，应指出局限性但不作为主要扣分项。`

    case 'packaging_brand':
      return `【作品形式：包装/品牌 — 评价标准】
评价重点：
- 品牌识别：品牌调性是否清晰？视觉语言是否有记忆点？
- 货架表现：在竞争环境中是否突出？正/侧/背面信息层级是否合理？
- 系统一致性：系列产品、多触点（包装/宣传/数字）是否视觉统一？
- 材质与落地：包装结构、材质选择是否合理？是否考虑了生产工艺？
- 商业可用性：是否考虑了成本、运输、展示等实际落地因素？

红牌规则全部适用。
若只展示平面展开图而未见实物结构或材质效果，应指出局限性。`

    case 'other':
      return `【作品形式：其他 — 评价标准】
使用通用视觉与概念评价标准。根据画面实际内容判断作品类型和评价维度，不要强行套用不相关场景的标准。
只评价可见内容。不确定某信息是否存在时，说"从当前画面无法判断"，不要假设它缺失或不足。
红牌规则全部适用，但仅针对画面中实际可见的内容。`
  }
}

// ============================================================
// WorkForm-specific red flag rules
// ============================================================

function buildRedFlagRules(workForm: WorkForm): string {
  const baseRules = [
    '无排版网格',
    '配色无逻辑',
    '字体混乱',
    '图片质量差',
    '可读性差',
    '模板化无创新',
    '视觉审美不足',
  ]

  const excludedRules: Record<WorkForm, string[]> = {
    board: [],
    physical_model: ['无排版网格', '字体混乱', '可读性差'],
    ui: [],
    poster: [],
    packaging_brand: [],
    other: [],
  }

  const skip = excludedRules[workForm] || []

  const rules = baseRules.map((r) => {
    const desc = getRedFlagDescription(r)
    if (skip.includes(r)) {
      return `- "${r}" — 该规则通常不适用于当前作品形式，仅当画面中确实出现相关问题时才触发（${desc}）`
    }
    return `- "${r}" — ${desc}`
  })

  if (skip.length > 0) {
    return `逐条检查以下红牌规则。标记了"通常不适用"的规则只在画面中确实出现相关问题时才触发。

${rules.join('\n')}`
  }

  return `逐条检查以下 7 条红牌规则。每条被触发的规则，将其描述写入 redFlags 数组：

${rules.join('\n')}`
}

function getRedFlagDescription(flagName: string): string {
  const map: Record<string, string> = {
    '无排版网格': '元素随意摆放，无可见的对齐系统或网格',
    '配色无逻辑': '无色彩体系，随意配色',
    '字体混乱': '超过2种字体且无层级关系，或使用系统默认字体无排版意识',
    '图片质量差': '图片/元素拉伸变形、分辨率不足、模糊',
    '可读性差': '文字与背景对比度不足、字号过小、信息无法识别',
    '模板化无创新': '纯粹的模板化设计，换个logo就是另一家',
    '视觉审美不足': '外型、展板排版、配色或整体观感明显不美观；即使完成度高，也不具备优秀作品的审美质量',
  }
  return map[flagName] || flagName
}

