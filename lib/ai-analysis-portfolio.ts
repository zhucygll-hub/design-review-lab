export function buildPortfolioAnalysisPrompt(
  targetCompany?: string,
  targetRole?: string,
  jobDescription?: string
): { system: string; user: string } {
  const hasTarget = targetCompany || targetRole

  let userText = '请严格按照评分流程（整体判断→红牌检查→维度评分→等级判定→calibrationNote），逐维度分析这份设计作品集。'

  if (hasTarget) {
    userText += '\n\n【目标岗位信息】'
    if (targetCompany) userText += `\n目标公司：${targetCompany}`
    if (targetRole) userText += `\n目标岗位：${targetRole}`
    if (jobDescription) userText += `\n岗位描述：${jobDescription}`
  }

  userText += '\n\n只返回 JSON，不要其他文字。'

  return {
    system: SYSTEM_PROMPT,
    user: userText,
  }
}
